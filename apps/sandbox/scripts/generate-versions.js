/* global require, console, __dirname */
/* eslint-disable no-undef, @typescript-eslint/no-require-imports */
/**
 * Generates apps/sandbox/src/generated/versionRegistry.ts from git tags.
 * Finds all tags matching 'docsite-*' and writes them as a typed array.
 */

const {execSync} = require('child_process');
const fs = require('fs');
const path = require('path');

const REPO_ROOT = path.resolve(__dirname, '..', '..', '..');
const OUT_DIR = path.resolve(__dirname, '..', 'src', 'generated');
const OUT_FILE = path.join(OUT_DIR, 'versionRegistry.ts');

function prettifyTagName(tag) {
  // Remove 'docsite-v' or 'docsite-' prefix
  const stripped = tag.replace(/^docsite-v?\d*-?/, '');
  if (!stripped) return tag;
  // Replace hyphens with spaces, title case
  return stripped
    .split('-')
    .map(w => w.charAt(0).toUpperCase() + w.slice(1))
    .join(' ');
}

function run() {
  let tagsRaw;
  try {
    tagsRaw = execSync('git tag -l "docsite-*"', {
      cwd: REPO_ROOT,
      encoding: 'utf-8',
    }).trim();
  } catch {
    tagsRaw = '';
  }

  if (!tagsRaw) {
    writeOutput([]);
    return;
  }

  const tags = tagsRaw.split('\n').filter(Boolean);

  const versions = tags.map(tag => {
    const dateStr = execSync(`git log -1 --format=%cI "${tag}"`, {
      cwd: REPO_ROOT,
      encoding: 'utf-8',
    }).trim();

    const message = execSync(
      `git tag -l --format="%(contents:subject)" "${tag}"`,
      {cwd: REPO_ROOT, encoding: 'utf-8'},
    ).trim();

    return {
      id: tag,
      name: prettifyTagName(tag),
      tag,
      message: message || undefined,
      timestamp: new Date(dateStr).getTime(),
    };
  });

  // Sort newest first
  versions.sort((a, b) => b.timestamp - a.timestamp);

  writeOutput(versions);
}

function writeOutput(versions) {
  fs.mkdirSync(OUT_DIR, {recursive: true});

  const entries = versions
    .map(v => {
      const fields = [
        `    id: ${JSON.stringify(v.id)}`,
        `    name: ${JSON.stringify(v.name)}`,
        `    tag: ${JSON.stringify(v.tag)}`,
        ...(v.message ? [`    message: ${JSON.stringify(v.message)}`] : []),
        `    timestamp: ${v.timestamp}`,
      ];
      return `  {\n${fields.join(',\n')},\n  }`;
    })
    .join(',\n');

  const content = `// Copyright (c) Meta Platforms, Inc. and affiliates.

// Auto-generated by scripts/generate-versions.js — do not edit
export type GitVersion = {
  id: string;
  name: string;
  tag: string;
  message?: string;
  timestamp: number;
};

export const gitVersions: GitVersion[] = [
${entries}
];
`;

  fs.writeFileSync(OUT_FILE, content, 'utf-8');
  console.log(
    `Wrote ${versions.length} version(s) to ${path.relative(REPO_ROOT, OUT_FILE)}`,
  );
}

run();
